       <q>Ou l’histoire d’un groupe de jeune… pas ordinaire !</q>  
               <h3>2010
                  LA CREATION</h3>  
                 <p style="background-color: white"> Parler de Diadème sans parler de nos ainés (l’équipe sénior) serait un manque de respect. Ils ont marqués l’histoire du club C.O.B en particulier et du basket-ball burkinabè en générale par leurs grands talents et leurs dévouements. Mais malheureusement le club connu des périodes difficiles ce qui entraina sa disparition en 2000. Les jeunes joueurs (minimes, cadets) que nous étions sommes resté orphelines et sans aucuns suivi ce qui entraina le retrait de plusieurs d’entre nous qui ont finis par abandonné ce sport malgré la bonne volonté de certaines ainés de poursuivent les activités.</p>

                 <p style="background-color: white">Cependant nous avons évolués dans les autres clubs de la ville et participant aux championnats et différentes  compétitions ; Mais l’envie de faire revivre le club et de jouer ensemble se faisait toujours sentir par les multiples rencontres entre jeunes joueurs dépourvus de soutiens; ces efforts n’ont pas cessé et nous n’avons pas manqué d’occasions pour se réunir et organisé des activités tels que les matchs amicaux et des tournois (Nuit du Basket) ; cela a favorisé nos relations avec des partenaires et des personnes ressources.</p>

                 <p style="background-color: white"> En 2009 nous avons eu la chance de faire la connaissance d’une ancienne gloire  du Basket-ball Burkinabè et ex-entraineur du C.O.B, DIARRA Mamadou (actuel Président et coach et professeur d’informatique à l’ESI de NAZI BONI) qui a accepter nous accompagné dans notre projet qui était de jouer ensemble dans une famille unis;
En 2010 après de multiples rencontres le Diadème Athlétique Club(D.A.C) voit le jour avec comme président DIARRA Mamadou et obtint son affiliation à la Fédération Burkinabè de Basket-ball (FEBBA) alors que nous évoluons toujours dans d’autres club. Dès la saison 2010-2011 le club engagea en catégorie minime une équipe garçons et une équipe fille au championnat national;</p>

          <p style="background-color: white">L’HISTOIRE EST EN ROUTE.

En 2013 nous avons décidé de nous réunir dans un même club et de mettre en place une équipe sénior homme. Les jeunes joueurs de la génération 2000 avaient grandi et étaient suffisamment aguerris. Les saisons s’enchaînent avec quasiment une nouvelle équipe supplémentaire chaque année, le club s’étoffe.
Dès les premiers matchs de la saison 2013-2014 les séniors Hommes se sont fait sentir par leurs déterminations et par la qualité de leurs prestations. Une nouvelle génération venait de naitre et déjà la quiétude s’installait dans les clubs adverses. 
Au cours de la saison 2014-2015 l’équipe se renforçât avec l’arrivée d’autres joueurs qui n’avaient pas pu se libérer de leurs clubs. Dès l’entament des compétitions les victoires s’enchainaient et aucuns clubs de la région de l’Ouest ne pouvait rivaliser. Nous avons terminé la saison en emportant la troisième place du championnat D1 sur le plan national et la première place sur le plan régional et ceux jusqu’à nos jours. 
 En 2015 le club est officiellement reconnu  et obtient son récépissé.</p>

