<p style="background-color: white">
    <em>L’EVOLUTION:</em>
Les années passent la gestion est de plus en plus prenante, les encadrants du club acquièrent de bonnes compétences et une forte maturité sportive. Les relations avec les autres clubs du pays se passent bien, le plus dur est de trouver de l’aide financière nécessaire pour poursuivre le projet.
<br>

     <em>AUJOURD’HUI:</em>
A ce jour, ce sont plus de 96 licenciés qui fleurissent les terrains les soirs de semaine pour les entrainements. Nos plus grand défis sont, remporter la première place du championnat national en sénior Homme car depuis 2015 nous n’avons pas pu dépasser notre rang dû a de multiples problème rencontrer au niveau du District et au niveau de la FEBBA notamment la gestion du championnat, trouver des sponsors et des partenaires en fin de poursuivre notre projet. Nous espérons participer également à au moins un tournoi international dans toutes les catégories.
